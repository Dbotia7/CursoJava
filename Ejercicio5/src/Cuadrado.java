public class Cuadrado {
    public static void main(String[] args) {
        // Imprimir los primeros 10 números cuadrados
        for (int i = 1; i <= 10; i++) {
            int cuadrado = i * i; // Calcula el cuadrado de i
            System.out.println(i + "² = " + cuadrado); // Imprime el resultado
        }
    }
}